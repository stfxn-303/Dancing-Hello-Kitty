# Clear screen function
def clear():
    os.system("cls" if os.name == "nt" else "clear")

# Animate function
def animate_wave(ascii_art, delay=0.05, amplitude=5, frequency=0.3):
    t = 0
    try:
        while True:
            clear()
            for i, line in enumerate(ascii_art):
                offset = int(math.sin(t + i * frequency) * amplitude)
                print(" " * offset + line)
            time.sleep(delay)
            t += 0.2
    except KeyboardInterrupt:
        clear()
        sys.exit()

# Run animation
animate_wave(hello_kitty_art)
